# Note 1: We start with a reminder to stay motivated
keep_going = True

print("Turning on the motivation machine...")

# Note 2: The Loop runs as long as keep_going is equal to True
while keep_going == True:
    print("Keep pushing! Your IT traineeship is getting closer! 🚀")
    
    # Note 3: The Emergency Stop
    keep_going = False 

print("Machine stopped safely.")
